#Use a dictionary to store information about a person you know.
#First name, last name, age, and the city in which they live.
#keys first_name, last_name, age, and city.
#Print each piece of information stored in your dictionary.

person = {'first_name': 'Dwayne', 'last_name': 'Johnson', 'age': 48, 'city': 'Southwest Ranches'}

print(f"This person's first name is {person['first_name']}!")

print(f"This person's last name is {person['last_name']}!")

print(f"This person's age is {person['age']}!")

print(f"This person's city is {person['city']}!")